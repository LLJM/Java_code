public class Assignmentoperator{
    public static void main(String args[]){
        int a=5;
        int b=3;
        b+=a;
        System.out.println("b+=a="+b);
        b=3;
        b-=a;
        System.out.println("b-=a="+b);
        b=3;
        b*=a;
        System.out.println("b*=a="+b);
        b=3;
        b/=a;
        System.out.println("b/=a="+b);
        b=3;
        b%=a;
        System.out.println("b%=a="+b);
    }
}